    private void bt_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_saveActionPerformed
        String question = tf_Question.getText();
        String category = "";
        String type = "";
        
        if (rb_sports.isSelected()) {
            category = "Deportes";
        } else if (rb_art.isSelected()) {
            category = "Arte"; 
        } else if (rb_entertaiment.isSelected()) {
            category = "Entretenimiento";
        } else if (rb_history.isSelected()) {
            category = "Historia";
        } else if (rb_geography.isSelected()) {
            category = "Geografia";
        } else if (rb_science.isSelected()) {
            category = "Ciencia";
        }
        
        if (rb_boolean.isSelected()) {
            type = "Booleana";
        } else if (rb_uniqueSelection.isSelected()) {
            type = "Seleccion unica"; 
        } else if (rb_multipleSelection.isSelected()) {
            type = "Seleccion multiple";
        }
        this.dispose();
    }//GEN-LAST:event_bt_saveActionPerformed

